package datatype;

public class Variables {

	public static void main(String[] args) {
		double x = 1.2;
		byte c = 2;
	//declare variable
		int g;
		//initialize variable
		g = 7;
		// in line declare and initialize variable
		//whole numbers
		byte a = 2; //1 byte for memory
		short b = 56;//2 bytes
		int age = 1;//4 bytes
		long e = 65L;// 8 bytes
		//decimals
		float z = 1.3F;//4 bytes
		double y = 1.2;//8 bytes
		//characters
		char j ='%';// 2bytes
		//boolean
		boolean i = true;//1byte
		
	}

}
